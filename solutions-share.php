		<h2>Integrate Across the Care Continuum</h2>
		<p>Effortlessly share clinical, administrative, and financial data across all care settings including acute, ambulatory, home care, hospice, long term care, behavioral health, and more. Multi-entity capabilities connect diverse facilities into one integrated EHR to effectively treat patient populations. Throughout the care continuum providers can simultaneously access a single, real-time patient record – including a shared MPI, eMAR, and allergy record. With solutions talking to one another in similar, self-developed programming languages, our applications not only integrate, but share a common look and feel.</p>

		<h2>Connect Using Point-to-Point Interfaces</h2>
		<p>If you aren’t up and running with a full MEDITECH EHR, that’s OK – we have a comprehensive, HL7-compliant interface library to handle data sharing with other vendor systems. Consisting of over 7, 200 interfaces our library not only includes interfaces, but reports, state tape, claims, electronic submission, and other programs. We cover your data exchange needs with detailed interface specifications and experience with numerous interface engines.</p>

		<h2>Communicate with Physician Practices</h2>
		<p>Practices on your network can enjoy all the benefits of our integrated, web-based ambulatory solution, which shares a single record across care settings. But what about unaffiliated practices using their own systems? No worries. MEDITECH’s EHR is well-versed in exchanging data with independent, divergent physician practice systems. We exchange data with third-party physician practices through Orders Inbound/Results and Reports Outbound interfaces, ADT feeds, and medical summaries--including support for direct messaging. We also work with Forward Advantage to provide a one-to-many interoperability suite that exchanges this information with multiple practice systems through a single set of interfaces.</p>

		<h2>Participate in State, Regional, and Community Data Exchanges</h2>
		<p>Let’s face it, your patients sometimes transition outside your network. Shouldn’t their data do the same? MEDITECH exchanges CCDs with non-MEDITECH systems and Record Locator Services (RLS). Searches for medical summaries occur in the background and can be discretely incorporated into you’re your patients’ records without disrupting workflow. We also support Direct Messaging with any HISP for secure communication.</p>
		<p>You can also contribute data to your community’s centralized HIE, view information available through external portals, and confirm patient identity through a third-party EMPI. Organizations looking to form their own HIE can use a full-fledged HIE solution developed with Forward Advantage, which includes not only the interfaces, but the infrastructure and services (i.e. Record Locator Service, Master Patient Index, Healthcare Provider Directory, and CCD Aggregator Toolset) to connect your organization with disparate systems – ultimately driving emerging ACOs.</p>

		<h2>Send and Submit Data to Public Health Agencies</h2>
		<p>Pre-built and automated public health reporting interfaces ensure the successful and ongoing submission of electronic data to immunization, state, cancer, and specialized registries. All submissions are generated from a certified MEDITECH EHR and follow HL7 2.5.1 specifications with electronic laboratory reporting adhering to LOINC and SNOMED CT data codes. Electronic results reporting is also available for pathology and blood bank.</p>

		<h2>Reduce Errors and Improve Patient Safety with e-Prescribing</h2>
		<p>No more faxing, hand writing, or calling in prescriptions. Our e-Prescribing interface connects with DrFirst’s eRx Gateway access to securely exchange patient information between various providers, payers, pharmacy benefit managers, and pharmacies. Providers can conveniently route electronic prescriptions to a patient’s pharmacy of choice and confirm insurance formulary status, coverage limits, and drug alternatives in real time. They can also access a patient’s medication claim history via DrFirst and SureScripts to reconcile medications and update the patient’s ambulatory medication profile.</p>


